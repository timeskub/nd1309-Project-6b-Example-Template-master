    initWeb3: async function () {
        /// Find or Inject Web3 Provider
        /// Modern dapp browsers...
        if (window.ethereum) {
            App.web3Provider = window.ethereum;
            try {
                // Request account access
                // await window.ethereum.enable();
                const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
                console.log("Connected with MM, accounts",accounts)
                // setAccounts(accounts);
            } catch (error) {
                // User denied account access...
                console.error("User denied account access")
                console.error(error)
            }
        }
        // Legacy dapp browsers...
        else if (window.web3) {
            App.web3Provider = window.web3.currentProvider;
        }
        // If no injected web3 instance is detected, fall back to Ganache
        else {
            // App.web3Provider = new Web3.providers.HttpProvider('http://localhost:7545');
            App.web3Provider = new Web3.providers.HttpProvider('http://localhost:9545');
        }

        App.getMetaskAccountID();

        return App.initSupplyChain();
    },

    getMetaskAccountID: function () {
        web3 = new Web3(App.web3Provider);

        // Retrieving accounts
        web3.eth.getAccounts(function(err, res) {
            if (err) {
                console.log('Error:',err);
                return;
            }
            console.log('getMetaskID:',res);
            App.metamaskAccountID = res[0];

        })
    },

    initSupplyChain: function () {
        /// Source the truffle compiled smart contracts
        var jsonSupplyChain='../../build/contracts/SupplyChain.json';
        /// JSONfy the smart contracts
        $.getJSON(jsonSupplyChain, function(data) {
            console.log('data',data);
            var SupplyChainArtifact = data;
            App.contracts.SupplyChain = TruffleContract(SupplyChainArtifact);
            console.log('App.contracts.SupplyChain', App.contracts.SupplyChain);
            App.contracts.SupplyChain.setProvider(App.web3Provider);
            console.log('App.web3Provider', App.web3Provider);
            
            App.fetchItemBufferOne();
            App.fetchItemBufferTwo();
            App.fetchEvents();


        });

        return App.bindEvents();
    },

    bindEvents: function() {
        $(document).on('click', App.handleButtonClick);
    },

    handleButtonClick: async function(event) {
        event.preventDefault();

        App.getMetaskAccountID();

        var processId = parseInt($(event.target).data('id'));
        console.log('processId',processId);

        switch(processId) {
            case 1:
                return await App.harvestItem(event);
                break;
            case 2:
                return await App.processItem(event);
                break;
            case 3:
                return await App.packItem(event);
                break;
            case 4:
                return await App.sellItem(event);
                break;
            case 5:
                return await App.buyItem(event);
                break;
            case 6:
                return await App.shipItem(event);
                break;
            case 7:
                return await App.receiveItem(event);
                break;
            case 8:
                return await App.purchaseItem(event);
                break;
            case 9:
                return await App.fetchItemBufferOne(event);
                break;
            case 10:
                return await App.fetchItemBufferTwo(event);
                break;
            case 11:
                return await App.addAllRoles(event);
                break;
            case 12:
                return await App.checkAllRoles(event);
                break;

            }
    },
    
    addAllRoles: async function (event) {
        event.preventDefault();
        var processId = parseInt($(event.target).data('id'));

        const instance = await App.contracts.SupplyChain.deployed();

        let result = await instance.addFarmer.call(App.originFarmerID, {from: App.metamaskAccountID});
        console.log("addFarmer", result);

        let result2 = await instance.addDistributor.call(App.distributorID, {from: App.metamaskAccountID});
        console.log("addDistributor", result2);
        
        let result3 = await instance.addRetailer.call(App.retailerID, {from: App.metamaskAccountID});
        console.log("addRetailer", result3);

        let result4 = await instance.addConsumer.call(App.consumerID, {from: App.metamaskAccountID});
        console.log("addConsumer", result4);

    },

    checkAllRoles: async function (event) {
        event.preventDefault();
        var processId = parseInt($(event.target).data('id'));

        const instance = await App.contracts.SupplyChain.deployed();

        let isFarmer = await instance.isFarmer(App.originFarmerID);
        let isDistributor = await instance.isDistributor(App.distributorID);
        let isRetailer = await instance.isRetailer(App.retailerID);
        let isConsumer = await instance.isConsumer(App.consumerID);

        console.log('isFarmer: ', isFarmer,', isDistributor:',isDistributor,', isRetailer:', isRetailer, ', isConsumer:', isConsumer);
    },


    harvestItem: function(event) {
        event.preventDefault();
        var processId = parseInt($(event.target).data('id'));

        App.contracts.SupplyChain.deployed().then(function(instance) {
            return instance.harvestItem.call(
                App.upc, 
                // App.metamaskAccountID,
                App.originFarmerID, 
                App.originFarmName, 
                App.originFarmInformation, 
                App.originFarmLatitude, 
                App.originFarmLongitude, 
                App.productNotes
            , {from: App.metamaskAccountID, gas: 4500000, gasPrice: 10000000000});
        }).then(function(result) {
            $("#ftc-item").text(result);
            console.log('harvestItem',result);
        }).catch(function(err) {
            console.log(err.message);
        });
    },

